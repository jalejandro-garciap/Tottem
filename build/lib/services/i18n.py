def set_lang(lang: str) -> None:
    global _LANG
    _LANG = "en" if str(lang).lower().startswith("en") else "es"


def toggle() -> str:
    global _LANG
    _LANG = "en" if _LANG == "es" else "es"
    return _LANG


def t(key: str, **kwargs) -> str:
    s = _STRINGS.get(_LANG, {}).get(key, key)
    if kwargs:
        try:
            return s.format(**kwargs)
        except Exception:
            return s
    return s


def current_lang() -> str:
    return _LANG

